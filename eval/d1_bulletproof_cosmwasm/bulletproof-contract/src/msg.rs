use cosmwasm_schema::{cw_serde, QueryResponses};
use cosmwasm_std::{Binary, Uint128};

// ---------------------------------------------------------------------------
// Instantiate
// ---------------------------------------------------------------------------

#[cw_serde]
pub struct InstantiateMsg {
    /// CW20 token name (e.g. "RangeBucket90-100").
    pub token_name: String,
    /// CW20 token symbol (e.g. "RB90").
    pub token_symbol: String,
    /// CW20 token decimals (typically 6).
    pub token_decimals: u8,
    /// Minimum active vault entries before a buyer may claim (k-anonymity).
    pub min_vault_depth: u64,
    /// Block-height grace period after the vault first enters a starved state
    /// (depth < min_vault_depth): once this many blocks have elapsed, the
    /// `min_vault_depth` check is bypassed (liveness fallback).
    /// Defaults to 100_000 (~7 days at 6 s blocks) if not specified.
    pub fallback_timeout_blocks: Option<u64>,
    /// Address of the authorized oracle relayer bot.
    /// Only this address may call `FulfillRandomness`.
    /// If not provided, the simulated oracle features (BurnAndRequest /
    /// FulfillRandomness) are unavailable and only the legacy atomic
    /// BurnAndClaim path is active.
    pub oracle_address: Option<String>,
    /// Block-height SLA window for the oracle relayer. If the oracle fails to
    /// call `FulfillRandomness` within this many blocks after `BurnAndRequest`,
    /// the buyer may call `RecoverOracleTimeout` to reclaim their burned token.
    /// Defaults to 100_800 (~7 days at 6 s blocks) if not specified.
    pub oracle_timeout_blocks: Option<u64>,
}

// ---------------------------------------------------------------------------
// Execute
// ---------------------------------------------------------------------------

#[cw_serde]
pub enum ExecuteMsg {
    /// Seller deposits an encrypted payload with an aggregated ($m{=}2$)
    /// Bulletproof range proof over 64 total bits ($2 \times 32$-bit dynamic
    /// boundaries via homomorphic shifting). The contract verifies the proof
    /// natively on-chain within the CosmWasm WebAssembly runtime and, if valid,
    /// atomically mints 1 CW20 Data-Backed Range Token to the depositor.
    /// Empirical cost: ~631K gas (4.8× compute multiplier vs CW-20 baseline).
    Deposit {
        /// Hex-encoded serialized Bulletproof range proof bytes.
        proof_hex: String,
        /// Hex-encoded serialized committed value (CompressedRistretto, 32 bytes).
        commitment_hex: String,
        /// Bit size used for the range proof (8, 16, 32, or 64).
        num_bits: u32,
        /// Identifier for the IPFS CID pointing to CT_data.
        ipfs_cid_hash: String,
        /// Identifier for the encrypted symmetric key (CT_key).
        ct_key_hash: String,
        /// Hex-encoded oracle signature attesting semantic binding between the
        /// data payload (ipfs_cid_hash) and the ZK commitment (commitment_hex).
        /// Conceptually: sign(oracle_sk, SHA256(ipfs_cid_hash || commitment_hex)).
        oracle_signature: String,
        /// Deterministic hash of the plaintext data generated by the DECO oracle,
        /// formatted as a 64-character lowercase hex string (SHA-256 digest).
        /// Prevents Sybil attacks where the same underlying dataset is deposited
        /// multiple times with randomized Bulletproof blinding factors. In
        /// production, the oracle attestation must cryptographically bind this
        /// nullifier alongside ipfs_cid_hash and commitment_hex.
        payload_nullifier: String,
    },

    // DEPRECATED: Legacy atomic BurnAndClaim (replaced by two-phase oracle flow)
    // /// Buyer burns 1 CW20 Range Token and atomically claims a randomly
    // /// selected vault payload.  Randomness is derived from the mocked ideal
    // /// functionality F_beacon (SHA-256 over block variables); see the
    // /// prominent warning in `contract.rs` for production caveats.
    // /// Requires at least `min_vault_depth` active entries (k-anonymity),
    // /// unless the fallback timeout has elapsed.
    // BurnAndClaim {
    //     /// The buyer's X25519 public key (hex-encoded, 32 bytes). Emitted in
    //     /// the on-chain Claim event so the TEE can strictly bind the key
    //     /// release to the authorized buyer, preventing off-chain hijacking.
    //     buyer_x25519_pubkey: String,
    // },

    /// **Phase 1 (Asynchronous Oracle Flow):** Buyer burns 1 CW20 Range Token
    /// and emits a `wasm-oracle_request` event for the off-chain relayer bot.
    /// The actual vault selection is deferred to `FulfillRandomness`.
    /// Reverts if a request is already pending for this sender.
    BurnAndRequest {
        /// The buyer's X25519 public key (hex-encoded, 32 bytes).
        buyer_x25519_pubkey: String,
    },

    /// **Phase 2 (Asynchronous Oracle Flow):** Called exclusively by the
    /// authorized oracle relayer to complete vault selection using a
    /// securely provided random seed. Loads and removes the pending
    /// request, then performs O(1) swap-and-pop vault selection.
    FulfillRandomness {
        /// The buyer's address whose pending request to fulfill.
        buyer_address: String,
        /// A secure 64-bit random seed provided by the off-chain relayer,
        /// encoded as a decimal string to avoid JSON precision loss for
        /// values exceeding JavaScript's Number.MAX_SAFE_INTEGER (2^53-1).
        random_seed: String,
    },

    /// **Timeout Recovery (Asynchronous Oracle Flow):** If the oracle relayer
    /// fails to call `FulfillRandomness` within `oracle_timeout_blocks` of
    /// the buyer's `BurnAndRequest`, the buyer may call this to reclaim
    /// their burned token. This guarantees that a crashed relayer cannot
    /// permanently destroy buyer funds.
    RecoverOracleTimeout {},

    // ── CW20 standard operations (forwarded to cw20-base) ─────────────
    /// Transfer CW20 tokens to another address.
    Transfer { recipient: String, amount: Uint128 },
    /// Send CW20 tokens to a contract with a message hook.
    Send {
        contract: String,
        amount: Uint128,
        msg: Binary,
    },
    /// Increase allowance for a spender.
    IncreaseAllowance {
        spender: String,
        amount: Uint128,
        expires: Option<cw20::Expiration>,
    },
    /// Decrease allowance for a spender.
    DecreaseAllowance {
        spender: String,
        amount: Uint128,
        expires: Option<cw20::Expiration>,
    },
    /// Transfer tokens from one address to another (requires allowance).
    TransferFrom {
        owner: String,
        recipient: String,
        amount: Uint128,
    },
    /// Send tokens from one address to a contract (requires allowance).
    SendFrom {
        owner: String,
        contract: String,
        amount: Uint128,
        msg: Binary,
    },
}

// ---------------------------------------------------------------------------
// Query
// ---------------------------------------------------------------------------

#[cw_serde]
#[derive(QueryResponses)]
pub enum QueryMsg {
    /// Returns the number of active (unclaimed) vault entries.
    #[returns(ActiveCountResponse)]
    ActiveCount {},

    /// Returns a specific vault entry by index.
    #[returns(VaultEntryResponse)]
    GetVaultEntry { index: u64 },

    /// Returns payload indices claimed by the given buyer.
    #[returns(ClaimedPayloadsResponse)]
    GetClaimedPayloads { buyer: String },

    /// Returns CW20 token info (name, symbol, decimals, total_supply).
    #[returns(cw20::TokenInfoResponse)]
    TokenInfo {},

    /// Returns CW20 balance for a given address.
    #[returns(cw20::BalanceResponse)]
    Balance { address: String },

    /// Returns the contract configuration.
    #[returns(ConfigResponse)]
    GetConfig {},

    /// Returns the pending oracle request for a given buyer, if any.
    #[returns(PendingRequestResponse)]
    GetPendingRequest { buyer: String },
}

// ---------------------------------------------------------------------------
// Query Responses
// ---------------------------------------------------------------------------

#[cw_serde]
pub struct ActiveCountResponse {
    pub count: u64,
}

#[cw_serde]
pub struct VaultEntryResponse {
    pub ipfs_cid_hash: String,
    pub ct_key_hash: String,
    pub proof_hash: String,
    pub num_bits: u32,
    pub depositor: String,
    pub active: bool,
}

#[cw_serde]
pub struct ClaimedPayloadsResponse {
    pub payload_indices: Vec<u64>,
}

#[cw_serde]
pub struct ConfigResponse {
    pub min_vault_depth: u64,
    pub owner: String,
    pub token_name: String,
    pub token_symbol: String,
    pub token_decimals: u8,
    pub total_supply: Uint128,
    pub fallback_timeout_blocks: u64,
    /// The authorized oracle relayer address, if configured.
    pub oracle_address: Option<String>,
    /// Block-height SLA window for the oracle relayer timeout recovery.
    pub oracle_timeout_blocks: u64,
}

#[cw_serde]
pub struct PendingRequestResponse {
    /// Whether a pending request exists for this buyer.
    pub found: bool,
    /// The range bucket ID (active vault count at request time), if found.
    pub range_bucket_id: Option<u64>,
    /// The block height at which the request was made, if found.
    pub request_height: Option<u64>,
}
