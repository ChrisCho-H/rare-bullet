use cosmwasm_std::Addr;
use cw_storage_plus::{Item, Map};
use schemars::JsonSchema;
use serde::{Deserialize, Serialize};

/// Global contract configuration set at instantiation.
#[derive(Serialize, Deserialize, Clone, Debug, PartialEq, JsonSchema)]
pub struct Config {
    /// Minimum active vault entries required before a buyer may claim (k-anonymity).
    pub min_vault_depth: u64,
    /// Contract administrator (set to the instantiator).
    pub owner: Addr,
    /// Block-height grace period: once this many blocks have elapsed since
    /// the vault first entered a starved state (`starvation_start_height`),
    /// the `min_vault_depth` check is bypassed so that buyers are not
    /// permanently locked out when the vault starves for new deposits
    /// (liveness over k-anonymity trade-off). Unlike a last-deposit-based
    /// timer, intermediate deposits that leave the depth below k do NOT
    /// reset the starvation timer, preventing liveness griefing attacks.
    pub fallback_timeout_blocks: u64,
    /// Block-height SLA window for the oracle relayer. If the oracle fails to
    /// call `FulfillRandomness` within this many blocks after `BurnAndRequest`,
    /// the buyer may call `RecoverOracleTimeout` to reclaim their burned token.
    /// Defaults to 100_800 (~7 days at 6 s blocks) if not specified.
    pub oracle_timeout_blocks: u64,
}

/// A pending randomness request awaiting fulfillment by the oracle relayer.
#[derive(Serialize, Deserialize, Clone, Debug, PartialEq, JsonSchema)]
pub struct PendingRequest {
    /// The range bucket identifier (currently the active vault count at request time).
    pub range_bucket_id: u64,
    /// The block height at which the request was made.
    pub request_height: u64,
    /// The buyer's X25519 public key (hex-encoded), forwarded from BurnAndRequest.
    pub buyer_x25519_pubkey: String,
}

/// A single vaulted data-asset payload.
#[derive(Serialize, Deserialize, Clone, Debug, PartialEq, JsonSchema)]
pub struct Payload {
    /// Opaque identifier for the IPFS CID (e.g., a hash or string provided by the depositor).
    pub ipfs_cid_hash: String,
    /// Opaque identifier for the encrypted symmetric key provided by the depositor.
    pub ct_key_hash: String,
    /// SHA-256 hash of the Bulletproof range proof bytes (hex-encoded).
    pub proof_hash: String,
    /// Bit size used for the range proof.
    pub num_bits: u32,
    /// Address of the depositor / data seller.
    pub depositor: Addr,
    /// Whether this vault entry is still active (unredeemed).
    pub active: bool,
}

pub const CONFIG: Item<Config> = Item::new("config");

/// Append-only registry of all payloads (index -> Payload).
pub const VAULT: Map<u64, Payload> = Map::new("vault");

/// Total number of payloads ever deposited (next index = vault_count).
pub const VAULT_COUNT: Item<u64> = Item::new("vault_count");

/// Indexed map of active (unclaimed) payload indices for true O(1) swap-and-pop.
/// Key: positional slot index (0..count-1), Value: payload ID in the vault.
pub const ACTIVE_PAYLOAD_MAP: Map<u64, u64> = Map::new("active_ids_map");

/// Total number of currently active payload entries.
pub const ACTIVE_PAYLOAD_COUNT: Item<u64> = Item::new("active_ids_count");

/// Payload indices claimed by each buyer address.
pub const CLAIMED_PAYLOADS: Map<&Addr, Vec<u64>> = Map::new("claimed");

/// Set of commitment hex strings that have already been used (replay prevention).
/// Key: commitment_hex, Value: true (presence indicates usage).
pub const USED_COMMITMENTS: Map<&str, bool> = Map::new("used_commits");

/// Set of payload nullifiers that have already been used (Sybil resistance).
/// A nullifier is a deterministic hash of the plaintext data generated by the
/// DECO oracle, preventing attackers from re-committing the same underlying
/// data with randomized blinding factors.
/// Key: nullifier string, Value: true (presence indicates usage).
pub const USED_NULLIFIERS: Map<&str, bool> = Map::new("used_nullifiers");

/// Block height at which the vault first entered a starved state (depth < k).
/// Set to `Some(height)` on a deposit when the active vault depth is below
/// `min_vault_depth` (including the very first deposit when depth is still below k).
/// While the depth remains below k, additional deposits do NOT reset the timer.
/// Reset to `None` when a new deposit pushes the depth to `>= min_vault_depth`.
/// The `BurnAndClaim` fallback bypasses the k-anonymity check only when
/// `current_height > starvation_start_height + fallback_timeout_blocks`,
/// preventing a griefing attack where an adversary trickles deposits to
/// perpetually reset a last-deposit-based timer.
pub const STARVATION_START_HEIGHT: Item<Option<u64>> = Item::new("starv_start_h");

/// Authorized oracle relayer address (set at instantiation).
/// Only this address may call `FulfillRandomness`.
pub const ORACLE_ADDRESS: Item<Addr> = Item::new("oracle_addr");

/// Map of pending randomness requests: buyer address → PendingRequest.
/// Populated by `BurnAndRequest`, consumed by `FulfillRandomness`.
pub const PENDING_REQUESTS: Map<&Addr, PendingRequest> = Map::new("pending_req");
